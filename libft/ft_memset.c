#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)b;
	while (n-- > 0)
	{
		*ptr++ = (unsigned char)c;
	}
	return (b);
}
